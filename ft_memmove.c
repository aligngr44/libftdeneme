#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	i = n;
	if (!dst && !src)
		return (NULL);
	if (dst == src)
		return (dst);
	if (n == 0)
		return (dst);
	if (d < s)
		ft_memcpy(d, s, n);
	if (d > s)
	{
		while (i > 0)
		{
			d[i - 1] = s[i - 1];
			i--;
		}
	}
	return (dst);
}
